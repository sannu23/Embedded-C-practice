/*
Variables are used to store data. They have a data type (int, float, char, etc.) and a name.
In C, you declare variables at the beginning of a function, typically before any code.
In C++, you can declare variables anywhere in a function.
Here's an example of declaring and using variables in both languages:*/

// C Example
#include <stdio.h>

int main() {
    int number = 42;
    float pi = 3.14159;
    char letter = 'A';

    printf("Number: %d\n", number);
    printf("Pi: %f\n", pi);
    printf("Letter: %c\n", letter);

    return 0;
}
